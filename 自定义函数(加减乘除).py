def add(num1,num2):
	print("%4.2f+%4.2f=%4.2f"%(num1,num2,num1+num2))
def sub(num1,num2):
	print("%4.2f-%4.2f=%4.2f"%(num1,num2,num1-num2))
def mul(num1,num2):
	print("%4.2f*%4.2f=%4.2f"%(num1,num2,num1*num2))
def div(num1,num2):
	print("%4.2f/%4.2f=%4.2f"%(num1,num2,num1/num2))
def power(num1,num2):
	print("%4.2f^%4.2f=%4.2f"%(num1,num2,num1**num2))

num1=float(input("输入第一个数:"))
num2=float(input("输入第二个数:"))
add(num1,num2)
sub(num1,num2)
mul(num1,num2)
div(num1,num2)
power(num1,num2)



     
